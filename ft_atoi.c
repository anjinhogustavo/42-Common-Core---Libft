#include "libft.h"

int	ft_atoi(const char *str)
{
	int	sn;
	int	i;
	int	res;

	i = 0;
	sn = 1;
	res = 0;
	while ((str[i] >= 7 && str[i] <= 13) || (str[i] == 32))
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
		{
			sn = -sn;
		}
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	return (res * sn);
}
